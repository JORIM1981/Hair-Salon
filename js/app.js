(function ($) {

    $(window).scroll(function () {

        if ($(this).scrollTop() < 50) {
            // hide nav
            $("nav").removeClass("ss-top-nav");
            $("#back-to-top").fadeOut();

        } else {
            // show nav
            $("nav").addClass("ss-top-nav");
            $("#back-to-top").fadeIn();
        }
    });
})(jQuery); // End of use strict